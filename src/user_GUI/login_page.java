    public boolean userLogin(String username) throws RemoteException {
         S_Chat c=new S_Chat();
         String[] allUsers = c.getUserList();
         
		for(int i = 0; i< allUsers.length; i++){
			users.set(i, allUsers[i]);
                        if(allUsers[i]==username){
                            JOptionPane.showMessageDialog(null, "This user is already exists");
            System.exit(0);
            return userloged=false;
                        }
                  
		} 
     
                  if (users.contains(username)) {
            JOptionPane.showMessageDialog(null, "This user is already exists");
            System.exit(0);
            return userloged=false;
            
        } 
        else {
            return userloged=true;
        } 
       
                        
         
    }

